#decodificar mensajes
msg="UNPRG"
for letra in msg:
     if letra == "U":
         print(os.sys.argv[1])
     if letra == "N":
         print(os.sys.argv[2])
     if letra == "P":
         print(os.sys.argv[3])
     if letra == "R":
         print(os.sys.argv[4])
     if letra == "G":
         print(os.sys.argv[5])

